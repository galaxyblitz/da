function switchOne(firstValue){
    var selectedValue = firstValue;

    switch(selectedValue){
        case 10:
        return "This case is 10";
        break;
        case 9:
        return "This case is 9";
        break;
        case 8:
        return "This case is 8";
        break;
        default:
        return "This case is default";
    }
}